// GetRubric retrieves a standard rubric by ID or returns the ID itself as a custom prompt if not found.
func GetRubric(idOrPrompt string) string {
	for _, r := range DefaultRubrics {
		if r.ID == idOrPrompt {
			return r.Prompt
		}
	}
	return idOrPrompt
}

// FormatInlineEvalPrompt builds a complete evaluation prompt for an inline harness check.
func FormatInlineEvalPrompt(rubricPrompt string, input string, targetOutput string, groundTruth string) string {
	var b strings.Builder
	b.WriteString(rubricPrompt)
	b.WriteString("\n\nInput Context:\n")
	b.WriteString(input)

	if groundTruth != "" {
		b.WriteString("\n\nExpected Ground Truth:\n")
		b.WriteString(groundTruth)
	}

	b.WriteString("\n\nTarget Output Generated by Model:\n")
	b.WriteString(targetOutput)

	return b.String()
}
