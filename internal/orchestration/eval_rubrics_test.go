package orchestration_test

import (
	"strings"
	"testing"

	"github.com/benjaminwestern/agentic-control/internal/orchestration"
)

func TestGetRubric(t *testing.T) {
	// Test known rubric
	prompt := orchestration.GetRubric("rubric-accuracy")
	if !strings.Contains(prompt, "factual accuracy") {
		t.Errorf("Expected rubric-accuracy to contain 'factual accuracy', got: %s", prompt)
	}

	// Test fallback to custom prompt
	custom := "This is a custom grading rubric."
	prompt = orchestration.GetRubric(custom)
	if prompt != custom {
		t.Errorf("Expected fallback to custom prompt, got: %s", prompt)
	}
}

func TestFormatInlineEvalPrompt(t *testing.T) {
	rubric := "Evaluate this."
	input := "Input text"
	target := "Target text"
	truth := "Ground truth"

	formatted := orchestration.FormatInlineEvalPrompt(rubric, input, target, truth)

	if !strings.Contains(formatted, rubric) {
		t.Errorf("Formatted prompt missing rubric")
	}
	if !strings.Contains(formatted, "Input Context:\n"+input) {
		t.Errorf("Formatted prompt missing input")
	}
	if !strings.Contains(formatted, "Expected Ground Truth:\n"+truth) {
		t.Errorf("Formatted prompt missing truth")
	}
	if !strings.Contains(formatted, "Target Output Generated by Model:\n"+target) {
		t.Errorf("Formatted prompt missing target")
	}
}

func TestFormatInlineEvalPromptNoTruth(t *testing.T) {
	formatted := orchestration.FormatInlineEvalPrompt("rubric", "input", "target", "")
	if strings.Contains(formatted, "Expected Ground Truth:") {
		t.Errorf("Formatted prompt should not contain ground truth section if empty")
	}
}
